        <h1>Conceitos sobre a Green IT</h1>

    <p>A TI Verde é um conceito amplo que se concentra em três grandes pontos: eficiência energética, redução de resíduos e preservação ambiental. Assim, sempre que ouvir falar neste termo, entenda que é possível focar em diferentes áreas, mas com o mesmo objetivo: a sustentabilidade.</p><br>

    <h2>1. Eficiência Energética</h2>

    <p>O setor de Tecnologia da Informação consome muita energia, por isso um dos pilares da TI Verde é alcançar uma melhor eficiência energética. Nesse caso, há um olhar atento para o uso de hardwares e softwares mais eficientes e otimização da infraestrutura.

Sendo assim, iniciativas como as da Ascenty, que investe em equipamentos de última geração e no uso de energia renovável nos Data Centers, são indispensáveis para o futuro do planeta.</p><br>

<h2>2. Redução de resíduos </h2>
<p>A TI Verde também dá uma atenção especial ao descarte em excesso e inadequado dos resíduos gerados pelo setor de tecnologia. Você já parou para pensar sobre o assunto?

Estamos tão acostumados a usar e descartar equipamentos eletrônicos, que não nos atentamos à quantidade de lixo que esse comportamento gera. Em ambientes empresariais, isso é ainda mais preocupante.

Neste ponto, vale a pena destacar o Programa Aterro Zero, da Ascenty. A iniciativa visa acabar com o descarte inadequado dos resíduos produzidos nos Data Centers, preservando o solo e os lençóis freáticos.</p><br>

<h2>3.  Preservação ambiental</h2>

<p>Por fim, o terceiro ponto é a preservação ambiental. Nesta área, o foco está em garantir uma evolução tecnológica e digital mais sustentável. Isso porque, é totalmente possível avançarmos com um menor impacto ambiental.

Em geral, as organizações devem priorizar decisões que não prejudiquem o meio ambiente e sempre pensar em maneiras de crescer com equilíbrio.</p><br><br><hr><br>


<h2>E por que é tão importante pensar em Tecnologia Verde?</h2>
<br>
<p>É simples: a pegada de carbono da era digital cresce a cada dia. Estudos apontam que a emissão de carbono associada às atividades digitais represente em torno de 4% das emissões totais no planeta e esse percentual deve crescer 6% ao ano até 2025.<br><br>

Estamos falando de 2 gigatoneladas de CO2 jogadas na atmosfera. Trazendo para um contexto mais real, isso equivale a 200 mil Torres Eiffel!<br><br>

Para neutralizar todo esse carbono, teríamos que cobrir 1/3 da Terra com florestas. Esses são dados alarmantes, que enfatizam o quanto é importante fazer algo hoje e agora.<br><br>

Essa emissão tem várias origens, desde à extração e processamento de matéria-prima para a fabricação de equipamentos de TI ao transporte e consumo energético. Aliás, é indispensável entender que existem dois tipos de uso digital:
<li>
Uso local: seus próprios equipamentos, computadores e servidores.
</li>
<li>
Uso na nuvem: recursos que operam em servidores remotos.
</li><br>

Nem sempre pensamos no consumo e impacto ambiental da cloud computing, especialmente por não estar no nosso campo de visão. Entretanto, manter servidores e dados na nuvem exige Data Centers e muito esforço tecnológico.
</p>
